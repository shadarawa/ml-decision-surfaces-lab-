    family = get_model_family(base_model)

    fig = plt.figure(figsize=(18, 10))
    gs = gridspec.GridSpec(3, 3, height_ratios=[0.6, 2, 2], figure=fig)

    ax_sum = fig.add_subplot(gs[0, :])
