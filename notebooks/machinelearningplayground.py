            model_name,
            C=C,
            gamma=gamma,
            svm_degree=svm_degree,
            n_neighbors=n_neighbors,
            weights=weights,
            max_depth=max_depth,
            min_samples_split=min_samples_split,
            n_estimators=n_estimators,
            hidden_layer_sizes=hidden_layer_sizes,
