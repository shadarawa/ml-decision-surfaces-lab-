    model_name,
    dataset,
    poly_degree,
    noise_pct,
    outliers_pct,
    n_samples,
    test_size,
    random_state,
    azimuth,
    elevation,
    alpha_points,
    point_size,
    colormap,
    C,
    gamma,
    svm_degree,
    n_neighbors,
    weights,
    max_depth,
    min_samples_split,
    n_estimators,
    hidden_layer_sizes,
    mlp_alpha,
    learning_rate_init,
    animate,
    anim_interval,
